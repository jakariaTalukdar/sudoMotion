// Helper functions to filter feedback
export const getFeedbackByService = (serviceSlug) => {
  return clientFeedback.filter(feedback => feedback.serviceSlug === serviceSlug);
};

export const getFeedbackByProduct = (productSlug) => {
  return clientFeedback.filter(feedback => feedback.productSlug === productSlug);
};

export const getFeaturedFeedback = (limit = 5) => {
  return [...clientFeedback]
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);
};
